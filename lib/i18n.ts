function detectLocale(): string {
  try {
    const loc = Intl.DateTimeFormat().resolvedOptions().locale.toLowerCase();
    if (loc.startsWith('pt')) return 'pt-BR';
    if (loc.startsWith('fr')) return 'fr-FR';
    if (loc.startsWith('de')) return 'de-DE';
    if (loc.startsWith('it')) return 'it-IT';
    if (loc.startsWith('es')) return 'es-ES';
    return 'en';
  } catch {
    return 'pt-BR';
  }
}

let _locale: string = detectLocale();

export function getLocale(): string { return _locale; }
export function setAppLocale(code: string): void { _locale = code; }

